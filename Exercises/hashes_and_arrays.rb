# hashes_and_arrays.rb
# 10. Can hash values be arrays? Can you have an array of hashes?
# (give examples)

# Yes, hash values can be arrays. Example:

h = {flowers: ["tulips", "roses", "daisies"], trees: ["beech", "pine", "willow"]}
p h


# Yes, you can have an array of hashes. Example:

arr = [{zero: "zero", one: "one"}, {a: "eh", b: "bee"}]
puts arr